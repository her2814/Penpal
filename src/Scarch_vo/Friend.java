package Scarch_vo;

public class Friend {
	private String userID;
	private String friendID;
	
	public String getUserID() {
		return userID;
	}
	public Friend setUserID(String userID) {
		this.userID = userID;
		return this;
	}
	public String getFriendID() {
		return friendID;
	}
	public Friend setFriendID(String friendID) {
		this.friendID = friendID;
		return this;
	}	
	
}
